//Huruf D

var s = "";
for (var i = 3; i > 0; i--) {
  for (var j = 4; j >= i; j--) {
    s += "DW";
  }
  s += "\n";
}
for (var k = 2; k > 0; k--) {
  for (var m = -1; m < k; m++) {
    s += "DW";
  }
  s += "\n";
}
console.log(s);

